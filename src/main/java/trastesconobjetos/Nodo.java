public class Nodo {
    
    private Traste valor; 
    private Nodo siguiente;

    public Nodo() {
        this.valor = valor;
        this.siguiente = siguiente;
    }

    public Traste getValor() {
        return valor;
    }

    public void setValor(Traste valor) {
        this.valor = valor;
    }

    public Nodo getSiguiente() {
        return siguiente;
    }

    public void setSiguiente(Nodo siguiente) {
        this.siguiente = siguiente;
    }


    
    
}
